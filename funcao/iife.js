//IIFE -> Immediately Invoked Function Expression

//É uma função que é executada imediatamente após sua criação.
//Ela deve estar entre parênteses e ser seguida por um par de parênteses tbm, conforme a sintaxe abaixo.

//EXEMPLO
(function() {
    console.log('Será executado na hora!')
    console.log('Foge do escopo mais abrangente!')
})()